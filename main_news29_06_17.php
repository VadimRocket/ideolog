						Лауреаты специальных фондов обладают правом: <br>

						-  льгот при строительстве жилья;<br>

						 -  право на первоочередное распределение;<br>

						 - право на первоочередное заселение в общежитие;<br>

						 - повышение тарифных ставок (окладов) на 50 % выпускникам, направленным по распределению в бюджетные организации;<br>

						- выбора из имеющихся на распределении мест работы;<br>

						- поступление в вуз или магистратуру без вступительных испытаний;<br>

						- повышение оклада на 15%, если они работают по направлению в бюджетной организации. Они производятся в течение двух лет с момента <br>заключения с ними трудового договора (контракта).
