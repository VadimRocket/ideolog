					<p>29 июня 2017 года выпускницам МГПУ им. И.П.Шамякина  Колосовской Илоне (филологического факультета), Пиляк Ксении (физико-инженерного факультета), Ширко Анне (филологического факультета),  были вручены свидетельства специального фонда Президента Республики Беларусь по социальной поддержке одаренных учащихся и студентов. </p>    

					<p> Мы от всей души поздравляем своих лауреатов Специального фонда Президента Республики Беларусь по социальной поддержке одаренных учащихся и студентов и желаем им новых успехов.</p>   

					<p> В 2016-2017 учебном году ряды одаренной молодежи нашего университета пополнили первокурсники Ковалев Максим и Гурко Максим, студенты технолого-биологического факультета. Также стипендиатом специального фонда Президента Республики Беларусь по социальной поддержке одаренных учащихся и студентов в этом учебном году стал Артюшко Николай, студент филологического факультета.</p>   

