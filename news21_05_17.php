					<p>В общежитии № 3 студентка филологического факультета Алламурадова Нургозель провела информационный час для белорусских студентов: «Новруз Байрам. История и традиции», рассказала об истории возникновения старинного праздника, который символизирует обновление человека и природы.</p>
