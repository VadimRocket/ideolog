					<h4 class="text-center">Новруз Байрам в МГПУ им. И.П.Шамякина </h4>
					<p><strong class="red">21 марта </strong>туркменские студенты, обучающиеся в МГПУ им. И.П.Шамякина, посвятили празднованию своего национального праздника Новруз Байрам – начало весны и приход нового года.</p>
