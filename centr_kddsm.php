						<p>Центр культурно-досуговой деятельности студенческой молодёжи (ЦКДДСМ) университета тесно сотрудничает с отделом культуры Мозырского районного исполнительного комитета, городскими дворцами культуры и другими организациями города, оказывая им помощь в проведении культурно-массовых мероприятий, участвуя в районных конкурсах.</p>

