								 <p> В каждый сектор входят студенты от всех факультетов. Один раз в месяц проводится заседание студенческого самоуправления и школы «Лидер».</p>

						          <p>Целью деятельности студенческого самоуправления университета является привлечение студенческой молодежи к разработке и проведению мероприятий, направленных на улучшение социально-бытовой, культурно-массовой, физкультурно-оздоровительной работы, а также профилактика правонарушений, выполнение Правил внутреннего распорядка в общежитиях, организация досуга студентов, сотрудничество с администрацией вуза по актуальным проблемам. Такие формы работы студсамоуправления позволяют вовлечь студенчество в разнообразную социально-значимую деятельность.</p>

						         <p> Школа студенческого актива «Лидер» является составной частью системы студенческого самоуправления и ее деятельность направлена на развитие студентов первого курса самостоятельности, инициативы, ответственности в решении вопросов учебы, отдыха, организации быта, создания условий для открытости и сотрудничества.</p>
								
