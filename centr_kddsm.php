						<h3  class="mtb20 text-center tlt">Центр КДДСМ</h3>
					<section class="mtb20">
		          		  <p>Центр культурно-досуговой деятельности студенческой молодёжи (ЦКДДСМ) университета создает условия для приобщения молодежи к достижениям в области национальной культуры, литературы и искусства; вовлекает активную молодежь в творческий процесс университета, занимается оказанием практической помощи студенческой молодежи в организации свободного времени, культурного досуга.</p>
					</section>
					<p>Работа ЦКДДСМ осуществляется по следующим <strong>направлениям:</strong></p>
					<ul>
					    <li><span class="glyphicon glyphicon-pencil"></span> Организация культурно-массовой и досуговой деятельности в университете.</li>
					    <li><span class="glyphicon glyphicon-pencil"></span> Координация работы творческих объединений молодежи по интересам.</li></li>
					    <li><span class="glyphicon glyphicon-pencil"></span> Развитие студенческого самоуправления в университете.</li>
					</ul>
					<p>На протяжении 2015–2016 учебного года студенты университета приняли участие в республиканских, областных и районных конкурсах различного уровня организации:</p>

					
