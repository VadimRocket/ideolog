					<h4 class="text-center"> Мероприятия, приуроченные к 73-ей годовщине освобождения Республики Беларусь от немецко-фашистских захватчиков   </h4>

						<p>С 26 июня по 3 июля 2017 года в УО «Мозырский государственный педагогический университет им.И.П.Шамякина» проходили мероприятия, приуроченные к 73-ей годовщине освобождения Республики Беларусь от немецко-фашистских захватчиков.	 </p>
						<p>В преддверие праздника на факультетах были проведены кураторские и информационные часы, посвященные этому знаковому событию в истории нашей страны. Студенты, проживающие в общежитиях университета, занимались уборкой и благоустройством мест захоронений времен Великой Отечественной войны, поздравили с наступающим праздником ветеранов Великой Отечественной войны Пугалова Владимира Прокофьевича и Хрусталеву Юлию Александровну! </p>
						<p> 30 июня администрация, сотрудники и профсоюзные организации университета посетили Романову Екатерину Филипповну, бывшую сотрудницу университета, ветерана Великой Отечественной войны, пожелали крепкого здоровья, светлого неба над головой, вручили подарки и цветы. </p>
						<p>3 июля 2017 года администрация университета, деканы факультетов, представители структурных подразделений приняли участие в торжественных мероприятиях, проходивших в городе Мозыре в этот праздничный день. Более 150 студентов
						было задействовано в праздничном шествии, более 30 сотрудников и студентов приняли участие в акции «Беларусь их помнит» («Бессмертный полк»). </p>
